    public void atualizaValorTotal(int linha) {
        Compra obj = listCompra.get(linha);
        double valorTotal = 0;

        for (ItensCompra itensCompra : daoI.getLista()) {
            if (itensCompra.getCompra().equals(obj)) {
                valorTotal += itensCompra.getQntd() * itensCompra.getProduto().getPrecoVenda();
            }
        }
        txtValorTotal.setText(Double.toString(valorTotal));
    }

    public void atualizaTabela() {
        listCompra.clear();
        listCompra.addAll(dao.getLista());
        int linha = listCompra.size() - 1;
        if (linha >= 0) {
            tableCompra.setRowSelectionInterval(linha, linha);
            tableCompra.scrollRectToVisible(tableCompra.getCellRect(linha, linha, true));
            atualizaValorTotal(linha);
        }
    }

