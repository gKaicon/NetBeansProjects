    public boolean validaCampos() {
        if (!(txtData.getText().length() > 0)) {
            JOptionPane.showMessageDialog(null, "Informe a Data de Compra");
            txtData.requestFocus();
            return false;
        }
        if (!(cbFuncionario.getSelectedIndex() >= 0)) {
            JOptionPane.showMessageDialog(null, "Informe o Funcionario responsável pela compra");
            cbFuncionario.requestFocus();
            return false;

        }
        if (!(cbFornecedor.getSelectedIndex() >= 0)) {
            JOptionPane.showMessageDialog(null, "Informe o Fornecedor de Compras da compra");
            cbFornecedor.requestFocus();
            return false;
        }
        return true;
    }

