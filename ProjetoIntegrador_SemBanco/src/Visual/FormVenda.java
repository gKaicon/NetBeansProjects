    public void atualizaValorTotal(int linha) {
        Venda obj = listVenda.get(linha);
        double valorTotal = 0;
        for (ItensVenda itensVenda : daoI.getLista()) {
            if (itensVenda.getVenda().equals(obj)) {
                valorTotal += itensVenda.getQntd() * itensVenda.getProduto().getPrecoVenda();
            }
        }
        txtValorTotal.setText(Double.toString(valorTotal));
    }

    public void atualizaTabela() {

        listVenda.clear();
        listVenda.addAll(dao.getLista());
        int linha = listVenda.size() - 1;
        if (linha >= 0) {
            tableVenda.setRowSelectionInterval(linha, linha);
            tableVenda.scrollRectToVisible(tableVenda.getCellRect(linha, linha, true));
            atualizaValorTotal(linha);
        }
    }

