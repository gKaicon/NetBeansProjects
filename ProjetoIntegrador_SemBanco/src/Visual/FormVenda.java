    public boolean validaCampos() {
        if (!(txtData.getText().length() > 0)) {
            JOptionPane.showMessageDialog(null, "Informe a Data de Venda");
            txtData.requestFocus();
            return false;
        }
        if (!(cbFuncionario.getSelectedIndex() >= 0)) {
            JOptionPane.showMessageDialog(null, "Informe o Funcionario responsável pela Venda");
            cbFuncionario.requestFocus();
            return false;

        }
        if (!(cbCliente.getSelectedIndex() >= 0)) {
            JOptionPane.showMessageDialog(null, "Informe o Cliente responsável pela Venda");
            cbCliente.requestFocus();
            return false;
        }
        return true;
    }

